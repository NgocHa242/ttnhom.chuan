    private void tbPhongBanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPhongBanMouseClicked

        
        ProcessCrt(true);
        this.btAdd.setEnabled(false);
        
        try
        {
            // lấy ra dòng dữ liệu vừa clcik chuột và trỏ nó đến id của nó 
            int row=this.tbPhongBan.getSelectedRow();
            String IDRow=(this.tbPhongBan.getModel().getValueAt(row, 0)).toString();
            String sql1="SELECT * FROM qlns.phongban where idPhongban='"+IDRow+"'";
            // đọc dữ liệu có mã là idRow rồi ghi nó lên cách textfield và combobox
            ResultSet rs = UpdateTable.ShowData(sql1);
            if(rs.next())// nêys có dữ liệu
            {
                id=rs.getString("idPhongban");
                this.txtMaPB.setText(rs.getString("idPhongban"));
                this.txtName.setText(rs.getString("name"));
                this.txtAddress.setText(rs.getString("Address"));
                this.txtPhone.setText(rs.getString("phone"));
            }
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Thông báo lỗi");
            
        }
    }//GEN-LAST:event_tbPhongBanMouseClicked

