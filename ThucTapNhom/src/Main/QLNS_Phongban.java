    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
       if(this.txtMaPB.getText().length()==0)
           JOptionPane.showMessageDialog(null, "Mã phòng ban không được bỏ trống","Thông Báo",1);
       else
           if(this.txtName.getText().length()==0 || this.txtAddress.getText().length()==0 || this.txtPhone.getText().length()==0 )
               JOptionPane.showMessageDialog(null, "Bạn chưa nhập đủ dữ liệu","Thông Báo",1);
       else
               if(this.txtMaPB.getText().length() >10 || this.txtPhone.getText().length() >11 )
                    JOptionPane.showMessageDialog(null, "Bạn nhập quá số kí tự Mã Phòng Ban và Phone","Thông Báo",1);
       else
               {
                  PhongBan.InsertPB(this.txtMaPB.getText().trim(), this.txtName.getText(), this.txtAddress.getText(), this.txtPhone.getText());
                  ShowBG();
                   ProcessCrt(false);
               }
    }//GEN-LAST:event_btAddActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        if(this.txtMaPB.getText().length()==0)
           JOptionPane.showMessageDialog(null, "Mã phòng ban không được bỏ trống","Thông Báo",1);
       else
           if(this.txtName.getText().length()==0 || this.txtAddress.getText().length()==0 || this.txtPhone.getText().length()==0 )
               JOptionPane.showMessageDialog(null, "Bạn chưa nhập đủ dữ liệu","Thông Báo",1);
       else
               if(this.txtMaPB.getText().length() >10 || this.txtPhone.getText().length() >11 )
                    JOptionPane.showMessageDialog(null, "Bạn nhập quá số kí tự Mã Phòng Ban và Phone","Thông Báo",1);
       else
               {
                  // lấy id ở bộ nhớ tạm ra so sánh để update lại
                   PhongBan.UpdatePB(this.txtMaPB.getText().trim(), this.txtName.getText(), this.txtAddress.getText(), this.txtPhone.getText(),id);
                  ShowBG();
                   ProcessCrt(false);
               }
    }//GEN-LAST:event_btEditActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
       if(this.txtMaPB.getText().length()==0)
           JOptionPane.showMessageDialog(null, "Không tìm thấy Mã phòng ban nào cần xóa","Thông Báo",1);
       else
       {
           if(JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa ?","Thông báo",2)==0)
           {
               PhongBan.DeletePB(id);
               ShowBG();
               ProcessCrt(false);
           }
       }
    }//GEN-LAST:event_btDeleteActionPerformed

